    private void actualizarTabla(){
        int tam = farmacia.getR_empleados().getEmpleados().size();
        String matriz[][] = new String[tam][5];
        for (int i = 0; i < tam; i++) {
            matriz[i][0] = farmacia.getR_empleados().getEmpleados().get(i).getDni();
            matriz[i][1] = farmacia.getR_empleados().getEmpleados().get(i).getNombre();
            matriz[i][2] = farmacia.getR_empleados().getEmpleados().get(i).getCargo();
            matriz[i][3] = farmacia.getR_empleados().getEmpleados().get(i).getTelefono();
            matriz[i][4] = String.valueOf(farmacia.getR_empleados().getEmpleados().get(i).getSueldo());

        }

        tabla.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]{
                    "DNI", "Nombre", "Cargo", "Telefono", "Sueldo"
                }
        ));
    }
