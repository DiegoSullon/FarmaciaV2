    private void eliminarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarButtonMouseClicked
        try {
            if (tabla.getSelectedRow() >= 0 && tabla.getSelectedColumn() > 0) {
                farmacia.getR_empleados().eliminarEmpleado(tabla.getValueAt(tabla.getSelectedRow(), 0).toString(),farmacia);
                actualizarTabla();
            }
        } catch (Exception e) {
            System.out.println("error eliminar");
        }

    }//GEN-LAST:event_eliminarButtonMouseClicked

    private void tablaPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_tablaPropertyChange

        try {
            if (tabla.getSelectedRow() >= 0 && tabla.getSelectedColumn() > 0 && tabla.getSelectedColumn() != 3) {

                farmacia.getR_empleados().modificarEmpleado(tabla.getValueAt(tabla.getSelectedRow(), 0).toString(), tabla.getColumnName(tabla.getSelectedColumn()).toLowerCase(), tabla.getValueAt(tabla.getSelectedRow(), tabla.getSelectedColumn()).toString().toLowerCase());
            }
        } catch (Exception e) {
            System.out.println("error change");
        }

    }//GEN-LAST:event_tablaPropertyChange

