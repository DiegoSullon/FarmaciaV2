    private void btnMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMenuActionPerformed

        int posi = btnMenu.getX();
        if(posi > 5){
            Animacion.Animacion.mover_izquierda(0, -300, 2, 2, panelMenu);
            Animacion.Animacion.mover_izquierda(295, 0, 2, 2, btnMenu);
        }
        else{
            Animacion.Animacion.mover_derecha(-300, 0, 2, 2, panelMenu);
            Animacion.Animacion.mover_derecha(0, 235, 2, 2, btnMenu);
        }
    }//GEN-LAST:event_btnMenuActionPerformed

